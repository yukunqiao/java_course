public class AddSum {
    public AddSum(int num1,int num2){
        int sum = num1 + num2;
        System.out.println("sum="+sum);
    }

    public AddSum(double num1,double num2){
        double sum = num1 + num2;
        System.out.println("sum="+sum);
    }

    public AddSum(String num1,String num2){
        String sum = num1 + num2;
        System.out.println("sum="+sum);
    }
}
